package com.neotech.review02;

public class Pyramid {

	public static void main(String[] args) {
		
		
		
		// *         print this
		// **
		// ***
		// ****
		
		
		for (int i =1; i<=4; i++)
		{
			
			for (int j = 1; j<=i; j++)
			{
				System.out.print("*");
			}
			System.out.println();
		}
	}

}
